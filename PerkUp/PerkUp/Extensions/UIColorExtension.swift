        let color = UIColor(red: 0/255, green: 118/255, blue: 165/255, alpha: 1)
        return color
    }
    class func perkupTableGreen() -> UIColor{
        let color = UIColor(red: 179/255, green: 240/255, blue: 222/255, alpha: 1)
